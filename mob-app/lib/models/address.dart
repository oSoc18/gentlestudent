class Address {
  final String addressId;
  final String street;
  final int housenumber;
  final int postalcode;
  final String city;
  final String bus;
  final double latitude;
  final double longitude;

  Address({
    this.addressId,
    this.street,
    this.city,
    this.postalcode,
    this.housenumber,
    this.bus,
    this.latitude,
    this.longitude,
  });
}
